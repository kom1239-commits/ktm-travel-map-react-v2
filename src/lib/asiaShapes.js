function toPath(coords) {
  return coords
    .map(([lng, lat], i) => {
      const { x, y } = project(lng, lat)
      return `${i === 0 ? 'M' : 'L'}${x.toFixed(1)},${y.toFixed(1)}`
    })
    .join(' ') + ' Z'
}

export const LAND_PATHS = REGIONS.map(toPath)
